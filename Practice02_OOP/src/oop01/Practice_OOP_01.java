		// 클래스란 객체(인스턴스)를 생성하는 하나의 공장(=틀,프레임,템플릿,판형)이다.
		// 시골 허허벌판에 농기계를 만들어내는 공장(class)이 들어섰고, 농부들이 주문을 넣으면 농기계가 만들어져 나온다.--> 그게바로 "객체"
		// 농기계 공장을 하나의 예로 들었을 뿐 얼마든지 내가 만들고자 하는 것을 클래스로 만들어낼 수 있다.
		// 자동차 클래스(공장), 동물 클래스(공장), AI로봇 클래스(공장)등등.. 만들고 싶은거 다 만들 수 있다.
		
		// 2. 클래스 공장을 통해서 객체를 어떻게 만들어내지?
		// 사람들이 공장에 주문을 넣는 과정을 장 생각해보면 답은 쉽게 나온다.
		// 예를 들면, 어떤 농부가 경작용 트랙터를 주문한다고 하면 가격을 고민하고, 색상도 고민하고, 연식도 고민할 것이다.
		// 뿐만 아니라, 트랙터의 기능이나 성능 등 동작들에 대해서도 많은 고민을 할 것이다.
