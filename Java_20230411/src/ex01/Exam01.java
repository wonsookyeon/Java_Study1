public class Exam01 {

	public static void main(String[] args) {

		for (int i=0; i<3; i++)  // i는 (0,1,2) 3번 실행  
			for (int j=0; j<5; j++) // j 는 (0,1,2,3,4) 5번 실행
				System.out.println(i + "+" + j);
		
		
		/* i가 0 일때 j는 0,1,2,3,4
		 * i가 1 일때 j는 0,1,2,3,4 실행된다.
		 */
		
		
		
		
	}

}
